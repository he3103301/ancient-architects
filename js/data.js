// 获取URL参数
function getUrlParam(name) {
  const urlParams = new URLSearchParams(window.location.search);
  return urlParams.get(name);
}

// 收藏相关函数
function getFavorites() {
  const stored = localStorage.getItem('architectFavorites');
  return stored ? JSON.parse(stored) : [];
}

function saveFavorites(favorites) {
  localStorage.setItem('architectFavorites', JSON.stringify(favorites));
}

function isFavorite(id) {
  return getFavorites().includes(id);
}

function addFavorite(id) {
  const favorites = getFavorites();
  if (!favorites.includes(id)) {
    favorites.push(id);
    saveFavorites(favorites);
    return true;
  }
  return false;
}

function removeFavorite(id) {
  let favorites = getFavorites();
  const index = favorites.indexOf(id);
  if (index > -1) {
    favorites.splice(index, 1);
    saveFavorites(favorites);
    return true;
  }
  return false;
}

function toggleFavorite(id) {
  if (isFavorite(id)) {
    removeFavorite(id);
    return false;
  } else {
    addFavorite(id);
    return true;
  }
}

// 搜索函数
function searchArchitects(keyword) {
  if (!keyword) return architects;
  const kw = keyword.toLowerCase();
  return architects.filter(item => {
    return item.name.includes(kw) ||
           item.dynasty.includes(kw) ||
           item.title.includes(kw) ||
           item.summary.includes(kw) ||
           item.works.some(work => work.includes(kw));
  });
}

// 获取所有朝代
function getAllDynasties() {
  const dynasties = [...new Set(architects.map(a => a.dynasty))];
  return dynasties.sort();
}

// 按朝代筛选
function filterByDynasty(dynasty) {
  if (!dynasty) return architects;
  return architects.filter(item => item.dynasty === dynasty);
}